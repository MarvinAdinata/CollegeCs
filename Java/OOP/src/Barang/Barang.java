package Barang;

public class Barang {
    private String nama;
    private double harga;
    private int jumlah;
    private int kode;

    

    public String getNama() {
        return nama;
    }
    public double getHarga() {
        return harga;
    }
    public int getJumlah() {
        return jumlah;
    }
    public int getKode() {
        return kode;
    }

    

}
